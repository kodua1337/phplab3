<?php
// Завдання 1: перевірка, чи число в діапазоні 1–10

// Отримуємо число з адресного рядка (наприклад: ?number=5)
if (isset($_GET['number'])) {
    $number = $_GET['number'];

    if (is_numeric($number) && $number >= 1 && $number <= 10) {
        echo "Число $number є дійсним і знаходиться у діапазоні від 1 до 10.";
    } else {
        echo "Помилка: введене число недійсне або не входить у діапазон від 1 до 10.";
    }
} else {
    echo "Будь ласка, вкажіть число через параметр <code>?number=...</code>";
}
?>
